import * as React from "react"
import { Slot } from "radix-ui"

import { cn } from "@/lib/utils"

// Each slot maps to a `.panel-*` shortcut class defined in `index.css`. The
// Tailwind utility chain ships via `@apply` inside the CSS rules — same
// pixels as the previous cva-style flat className, but the DOM shows a
// single readable class name (`<div class="panel-header">`).

function Card({
  className,
  asChild = false,
  ...props
}: React.ComponentProps<"div"> & { asChild?: boolean }) {
  const Comp = asChild ? Slot.Root : "div"
  return (
    <Comp
      data-slot="card"
      className={cn("panel", className)}
      {...props}
    />
  )
}

function CardHeader({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="card-header"
      className={cn("panel-header", className)}
      {...props}
    />
  )
}

